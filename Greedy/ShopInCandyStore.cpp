class Solution
{
public:
    vector<int> candyStore(int candies[], int N, int K)
    {
        // Write Your Code here
        sort(candies, candies + N);
        int res1 = 0, res2 = 0, i = 0, j = N-1, count = N;
        while(count > 0)
        {
            res1 += candies[i++];
            res2 += candies[j--];
            count -= K+1; 
        }
        return {res1, res2};
    }
};

